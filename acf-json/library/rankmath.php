add_action( 'rank_math/frontend/description', function( $description ) {
	global $post;
	

	$desc = RankMath\Post::get_meta( 'description', $post->ID );
	if ( ! $desc ) {
		//return  "no-description".$post->ID;
		return custom_wpseo_metadesc();
	}
	
	if ( ! $desc ) {
		//return  "description".$post->ID;
	$desc = RankMath\Helper::get_settings( "titles.pt_{$post->post_type}_description" );
		if ( $desc ) {
			return RankMath\Helper::replace_vars( $desc, $post );
		}
	}
   
	return $description;
   });

function custom_wpseo_metadesc() {
	global $post;
	$text = '';
	if (have_rows('page_layout', $post->ID)):
		while (have_rows('page_layout', $post->ID)) : the_row();
			$text .= get_sub_field('content');
		endwhile;
	elseif(is_singular('product')):
		$text .= get_field('content', $post->ID);
	else:
		$text .= get_post_field('post_content', $post->ID);
	endif; 
	if ( $text != '' ) {
		$text = strip_shortcodes($text);
		$text = strip_tags($text);
		$text = substr($text, 0, 155);
		$text = substr($text, 0, strripos($text, " "));
		$text = trim(preg_replace( '/\s+/', ' ', $text));
		$text = $text.'…';
	}
	return  $text;
	
}
add_filter('wpseo_metadesc', 'func_wpseo_metadesc', 10, 1);
function func_wpseo_metadesc($description)
{
	
	if($description == '' || empty($description) ){
		$description =  custom_wpseo_metadesc(); 
	}
  return $description;
}
